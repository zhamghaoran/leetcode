#include "bits/stdc++.h"

using namespace std;

class Solution {
public:
    int mostFrequentEven(vector<int> &nums) {
        vector<int> val;
        std::for_each(nums.begin(), nums.end(), [&](const auto &item) {
            if (!(item & 1))
                val.push_back(item);
        });
        std::sort(val.begin(), val.end());
        val.push_back(0x3f3f3f3f);
        int maxx = 0;
        int ans = -1;
        int now = 1;
        for (int i = 0;i < val.size() - 1;i ++) {
            if (val[i] == val[i + 1]) {
                now ++;
            } else {
                if (now > maxx && val[i] % 2 == 0) {
                    maxx = now;
                    ans = val[i];
                }
                now = 1;
            }
        }
        return ans;
    }
};

